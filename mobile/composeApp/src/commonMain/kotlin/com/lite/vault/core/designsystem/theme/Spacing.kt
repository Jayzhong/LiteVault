package com.lite.vault.core.designsystem.theme

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * Spacing Scale (8dp grid system)
 */
object AppSpacing {
    val xs: Dp = 4.dp    // Internal chip padding, very tight spacing
    val sm: Dp = 8.dp    // Tight spacing between related elements
    val md: Dp = 16.dp   // Default spacing, padding inside cards
    val lg: Dp = 24.dp   // Section spacing
    val xl: Dp = 32.dp   // Screen margins, large gaps
    val xxl: Dp = 48.dp  // Extra large gaps (between major sections)
}
